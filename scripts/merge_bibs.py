# Dataclasses
# ============================================================================

@dataclass(eq=False)
class Entry:
    sidecar: str
    local_tag: str  # synthetic: equals base_local_tag for occurrence 1, else base#N
    base_local_tag: str  # the original key as it appears in the sidecar
    entry_type: str
    fields: dict[str, str]

    @property
    def title(self) -> str:
        return self.fields.get("title", "")

    @property
    def identifiers(self) -> set[str]:
        if not hasattr(self, "_ids"):
            self._ids = extract_identifiers(self.fields)
        return self._ids

    @property
    def title_bow_set(self) -> set[str]:
        if not hasattr(self, "_bow"):
            self._bow = set(title_tokens(self.title))
        return self._bow

    @property
    def title_words(self) -> list[str]:
        if not hasattr(self, "_tw"):
            self._tw = title_tokens(self.title)
        return self._tw

    @property
    def lastname(self) -> str:
        if not hasattr(self, "_ln"):
            self._ln = first_author_lastname(self.fields.get("author", ""))
        return self._ln

    @property
    def year(self) -> str:
        if not hasattr(self, "_yr"):
            self._yr = extract_year(self.fields)
        return self._yr

    @property
    def ref(self) -> str:
        return f"{self.sidecar}:{self.local_tag}"


@dataclass(eq=False)
class Group:
    canonical: Entry
    merged: list[Entry] = field(default_factory=list)
    global_tag: str | None = None
    # Tags this group's assignment collided with (forced our extension past
    # the baseline <lastname><year><titleword1>). Populated by assign_one_tag.
    collided_tags: list[str] = field(default_factory=list)

    def all_entries(self):
        yield self.canonical
        yield from self.merged

    def add(self, entry: Entry) -> None:
        self.merged.append(entry)

    def make_canonical(self, entry: Entry) -> None:
        if entry is self.canonical:
            return
        if not any(e is entry for e in self.merged):
            raise ValueError("entry is not in this group")
        self.merged = [e for e in self.merged if e is not entry]
        old = self.canonical
        self.canonical = entry
        self.merged.append(old)


# ============================================================================
# Matching
# ============================================================================

@dataclass
class MatchResult:
    kind: str  # "definitive" | "canonical_equal" | "candidate" | "none"
    group: "Group | None" = None
    jaccard: float = 0.0


def jaccard(a: set[str], b: set[str]) -> float:
    if not a or not b:
        return 0.0
    return len(a & b) / len(a | b)


def canonicalise_for_equality(fields: dict[str, str]) -> dict[str, str]:
    out = {}
    for k, v in fields.items():
        kl = k.lower()
        if kl in NOISE_FIELDS:
            continue
        out[kl] = re.sub(r"\s+", " ", v.strip())
    return out


def canonical_equal(a: Entry, b: Entry) -> bool:
    return canonicalise_for_equality(a.fields) == canonicalise_for_equality(b.fields)


def find_match(entry: Entry, groups: list[Group]) -> MatchResult:
    best = MatchResult(kind="none")
    for g in groups:
        other = g.canonical
        if entry.identifiers and other.identifiers:
            if entry.identifiers & other.identifiers:
                return MatchResult(kind="definitive", group=g)
            continue  # definitively different — skip even title comparison
        if canonical_equal(entry, other):
            return MatchResult(kind="canonical_equal", group=g)
        if entry.title_bow_set and other.title_bow_set:
            j = jaccard(entry.title_bow_set, other.title_bow_set)
            if j >= JACCARD_THRESHOLD and j > best.jaccard:
                best = MatchResult(kind="candidate", group=g, jaccard=j)
    return best


