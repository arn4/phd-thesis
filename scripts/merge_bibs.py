def parse_sidecar(path: Path) -> list[Entry]:
    parser = BibTexParser(common_strings=True)
    parser.ignore_nonstandard_types = False
    parser.homogenize_fields = False
    parser.interpolate_strings = True
    with open(path, "r", encoding="utf-8", errors="replace") as f:
        db = bibtexparser.load(f, parser=parser)
    entries: list[Entry] = []
    occurrence_count: dict[str, int] = {}
    for raw in db.entries:
        base_tag = raw["ID"]
        occurrence_count[base_tag] = occurrence_count.get(base_tag, 0) + 1
        occ = occurrence_count[base_tag]
        # Synthetic local_tag for occurrences past the first, so refs stay
        # unique downstream. The base_tag is what the user's .tex source cites
        # and what the cite-rewriter will look up in the citation map.
        local_tag = base_tag if occ == 1 else f"{base_tag}#{occ}"
        if occ > 1:
            sys.stderr.write(
                f"[dup-local-tag] {path.name}:{base_tag} occurrence #{occ}; "
                "will be force-merged with the first occurrence's group "
                "(same base tag => only one entry can win)\n"
            )
        entry_type = raw["ENTRYTYPE"]
        fields = {
            k.lower(): v for k, v in raw.items() if k not in ("ID", "ENTRYTYPE")
        }
        entries.append(Entry(
            sidecar=path.name,
            local_tag=local_tag,
            base_local_tag=base_tag,
            entry_type=entry_type,
            fields=fields,
        ))
    return entries


def format_entry(g: Group) -> str:
    e = g.canonical
    lines = [f"@{e.entry_type.lower()}{{{g.global_tag},"]
    written = set()
    for fname in FIELD_ORDER:
        for k, v in e.fields.items():
            if k.lower() == fname and k not in written:
                lines.append(f"  {fname} = {{{v}}},")
                written.add(k)
                break
    for k in sorted(e.fields.keys()):
        if k in written:
            continue
        lines.append(f"  {k.lower()} = {{{e.fields[k]}}},")
    lines.append("}")
    return "\n".join(lines)


def write_bib(path: Path, groups: list[Group]) -> None:
    sorted_groups = sorted(groups, key=lambda g: g.global_tag or "")
    body = "\n\n".join(format_entry(g) for g in sorted_groups)
    path.write_text(HEADER + body + "\n", encoding="utf-8")


def save_progress(
    sidecars_dir: Path, sidecars: list[Path], groups: list[Group]
) -> None:
    """Persist the citation maps so an interrupted interactive run can resume.

    Only writes the maps (not the merged bib). On the next invocation, the
    existing-maps idempotency path picks up already-decided entries and skips
    re-prompting them.
    """
    write_maps(sidecars_dir, sidecars, groups)


def write_maps(sidecars_dir: Path, sidecars: list[Path], groups: list[Group]) -> None:
    by_sidecar: dict[str, dict[str, dict]] = {s.name: {} for s in sidecars}
    for g in groups:
        canonical_ref = g.canonical.ref
        merged_from = [e.ref for e in g.merged]
        for e in g.all_entries():
            if e.sidecar not in by_sidecar:
                continue
            if e is g.canonical:
                rec: dict = {
                    "global_tag": g.global_tag,
                    "reason": "assigned",
                }
                # Tag-disambiguation forensics: only present when the assigned
                # global_tag was extended past the baseline because some other
                # group already claimed that baseline.
                baseline = build_tag(g, 1) if g.global_tag else None
                if baseline and g.global_tag != baseline:
                    rec["tag_baseline"] = baseline
                if g.collided_tags:
                    rec["collided_with"] = list(g.collided_tags)
                # Cross-sidecar-duplicate forensics: which loser entries
                # merged into this canonical (the inverse of merged_into on
                # the losers' records).
                if merged_from:
                    rec["merged_from"] = list(merged_from)
                by_sidecar[e.sidecar][e.local_tag] = rec
            else:
                by_sidecar[e.sidecar][e.local_tag] = {
                    "global_tag": g.global_tag,
                    "reason": "merged-away",
                    "merged_into": canonical_ref,
                }
    for s in sidecars:
        map_path = sidecars_dir / f"{s.stem}-citation-map.json"
        data = {
            "sidecar": s.name,
            "mappings": dict(sorted(by_sidecar[s.name].items())),
        }
        map_path.write_text(
            json.dumps(data, indent=2, ensure_ascii=False) + "\n",
            encoding="utf-8",
        )


