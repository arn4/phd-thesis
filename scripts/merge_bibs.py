# ============================================================================
# Normalisation
# ============================================================================

def _decode_latex(s: str) -> str:
    try:
        return LATEX_DECODER.latex_to_text(s)
    except Exception:
        return s


def latex_clean_title(raw: str) -> str:
    """Strip LaTeX noise from a title — for matching/tag use ONLY, never for output."""
    text = re.sub(r"%.*", "", raw)
    text = re.sub(r"\$[^$]*\$", " MATH ", text)
    text = re.sub(r"\\\([^)]*\\\)", " MATH ", text)
    text = _decode_latex(text)
    text = text.replace("{", " ").replace("}", " ")
    text = text.replace("--", " ").replace("–", " ").replace("—", " ")
    text = unicodedata.normalize("NFKD", text)
    text = "".join(c for c in text if not unicodedata.combining(c))
    text = text.lower()
    text = re.sub(r"[^a-z0-9\s]", " ", text)
    return re.sub(r"\s+", " ", text).strip()


def title_tokens(raw: str) -> list[str]:
    return [t for t in latex_clean_title(raw).split() if t not in TITLE_STOPWORDS]


def normalize_doi(raw: str) -> str | None:
    if not raw:
        return None
    s = raw.strip().strip("{}").lower()
    s = re.sub(r"^(https?://)?(dx\.)?doi\.org/", "", s)
    s = re.sub(r"^doi:\s*", "", s)
    return s.strip().rstrip("/") or None


def normalize_eprint(raw: str) -> str | None:
    if not raw:
        return None
    s = raw.strip().strip("{}").lower()
    s = re.sub(r"^arxiv:\s*", "", s)
    s = re.sub(r"v\d+$", "", s)
    return s or None


def normalize_isbn(raw: str) -> str | None:
    if not raw:
        return None
    s = re.sub(r"[^0-9xX]", "", raw).lower()
    return s or None


def extract_identifiers(fields: dict[str, str]) -> set[str]:
    ids: set[str] = set()
    doi = normalize_doi(fields.get("doi", ""))
    if doi:
        ids.add(f"doi:{doi}")
        # arXiv-DOI bridge: 10.48550/arXiv.X is the same paper as eprint X
        m = re.match(r"^10\.48550/arxiv\.(.+)$", doi)
        if m:
            ids.add(f"eprint:{re.sub(r'v\d+$', '', m.group(1))}")
    eprint = normalize_eprint(fields.get("eprint", ""))
    if eprint:
        ids.add(f"eprint:{eprint}")
    isbn = normalize_isbn(fields.get("isbn", ""))
    if isbn:
        ids.add(f"isbn:{isbn}")
    return ids


# ============================================================================
# Author / lastname / year
# ============================================================================

def split_authors(author_str: str) -> list[str]:
    return [p.strip() for p in re.split(r"\s+and\s+", author_str, flags=re.IGNORECASE) if p.strip()]


def first_author_lastname(author_str: str) -> str:
    if not author_str:
        return "anon"
    authors = split_authors(author_str)
    if not authors:
        return "anon"
    first = authors[0]
    if "," in first:
        surname = first.split(",", 1)[0]
    else:
        tokens = first.split()
        surname = tokens[-1] if tokens else first
    surname = surname.replace("{", "").replace("}", "").strip()
    surname = _decode_latex(surname)
    surname = unicodedata.normalize("NFKD", surname)
    surname = "".join(c for c in surname if not unicodedata.combining(c))
    surname = surname.encode("ascii", "ignore").decode("ascii")
    surname = re.sub(r"[^a-z0-9]", "", surname.lower())
    return surname or "anon"


def extract_year(fields: dict[str, str]) -> str:
    raw = fields.get("year", "").strip().strip("{}")
    m = re.search(r"(\d{4})", raw)
    return m.group(1) if m else "nd"


