def auto_pick_canonical(entries: list[Entry]) -> Entry:
    """Tiebreaker chain for --no-stop and as the heuristic shown in the prompt."""
    def score(e: Entry):
        has_doi = bool(normalize_doi(e.fields.get("doi", "")))
        has_eprint = bool(normalize_eprint(e.fields.get("eprint", "")))
        published = bool(e.fields.get("pages") and e.fields.get("volume"))
        length = sum(
            len(v) for k, v in e.fields.items()
            if k.lower() not in {"abstract", "keywords"}
        )
        return (has_doi, has_eprint, published, length)
    return max(entries, key=score)


def _render_entry(entry: Entry, label: str) -> str:
    lines = [f"[{label}] {entry.ref}  (@{entry.entry_type})"]
    shown_keys = set()
    for fname in ("title", "author", "year", "journal", "booktitle",
                  "publisher", "doi", "eprint", "pages", "volume", "number"):
        for k, v in entry.fields.items():
            if k.lower() == fname and k not in shown_keys:
                shown_keys.add(k)
                lines.append(f"  {fname:10s}: {v}")
                break
    return "\n".join(lines)


def prompt_user(
    new_entry: Entry,
    group: Group,
    jaccard_score: float,
    *,
    force_merge: bool = False,
    progress_label: str | None = None,
) -> str:
    auto = auto_pick_canonical([group.canonical, new_entry])
    auto_label = "NEW" if auto is new_entry else "CURRENT"
    bar = "=" * 72
    print(f"\n{bar}", flush=True)
    if progress_label:
        print(f"[{progress_label}]", flush=True)
    if force_merge:
        print(
            f"In-sidecar duplicate local tag '{new_entry.base_local_tag}' "
            f"in {new_entry.sidecar} -- must pick one entry "
            "(the .tex source can't disambiguate)",
            flush=True,
        )
    else:
        print(f"Potential duplicate  (BoW Jaccard {jaccard_score:.2f})", flush=True)
    print(bar, flush=True)
    print(_render_entry(group.canonical, "CURRENT"), flush=True)
    print(flush=True)
    print(_render_entry(new_entry, "NEW"), flush=True)
    print(flush=True)
    print(f"Auto-heuristic would pick: {auto_label}", flush=True)
    print(flush=True)
    print("Choose:", flush=True)
    print("  [y] yes, merge - keep CURRENT as canonical", flush=True)
    print("  [r] yes, merge - replace canonical with NEW", flush=True)
    if not force_merge:
        print("  [n] no, these are distinct papers", flush=True)
    print(f"  [enter] accept auto-heuristic pick ({auto_label})", flush=True)
    print("  [q] quit (progress already saved; re-run to resume)", flush=True)
    valid = "y/r/q" if force_merge else "y/r/n/q"
    while True:
        try:
            ans = input("> ").strip().lower()
        except EOFError:
            return "quit"
        if ans == "":
            return "merge_replace" if auto is new_entry else "merge_keep_current"
        if ans in ("y", "yes"):
            return "merge_keep_current"
        if ans in ("r", "replace"):
            return "merge_replace"
        if ans in ("n", "no") and not force_merge:
            return "distinct"
        if ans in ("q", "quit"):
            return "quit"
        print(f"(answer {valid} or enter for auto)", flush=True)


def resolve_candidate(
    new_entry: Entry,
    group: Group,
    jaccard_score: float,
    no_stop: bool,
    *,
    force_merge: bool = False,
    progress_label: str | None = None,
) -> str:
    if no_stop:
        winner = auto_pick_canonical([group.canonical, new_entry])
        tag = "[auto:force]" if force_merge else "[auto]"
        if winner is new_entry:
            sys.stderr.write(
                f"{tag} {new_entry.ref} replaces canonical {group.canonical.ref}\n"
            )
            return "merge_replace"
        sys.stderr.write(
            f"{tag} {new_entry.ref} merged into {group.canonical.ref}\n"
        )
        return "merge_keep_current"
    return prompt_user(
        new_entry, group, jaccard_score,
        force_merge=force_merge, progress_label=progress_label,
    )


# ============================================================================
# Tag generation
# ============================================================================

def build_tag(g: Group, word_count: int) -> str:
    e = g.canonical
    words = e.title_words[:word_count] if e.title_words else ["untitled"]
    return f"{e.lastname}{e.year}{''.join(words)}"


def assign_one_tag(group: Group, claimed: set[str]) -> None:
    """Pick a global tag for `group`, avoiding any tag already in `claimed`.

    Baseline: <lastname><year><titleword1>. On collision, extend by adding
    the next cleaned title word; if title words run out, append a/b/c.
    Records every claimed tag we bumped into in `group.collided_tags`
    (forensic info written to the citation map). Mutates `group.global_tag`.
    (Incremental, called once per new group — so collisions among groups
    created in the same run are resolved asymmetrically, with whoever was
    created first keeping the shorter tag.)
    """
    max_words = max(len(group.canonical.title_words), 1)
    wc = 1
    collisions: list[str] = []
    while True:
        tag = build_tag(group, wc)
        if tag not in claimed:
            group.global_tag = tag
            group.collided_tags = collisions
            return
        collisions.append(tag)
        if wc < max_words:
            wc += 1
            continue
        # Title exhausted — append a/b/c suffix to the longest form.
        idx = 0
        while True:
            cand = tag + chr(ord("a") + idx)
            if cand not in claimed:
                group.global_tag = cand
                group.collided_tags = collisions
                return
            collisions.append(cand)
            idx += 1


