def precount_prompts(
    sidecars: list[Path],
    entries_by_sidecar: dict[str, list[Entry]],
    existing_maps: dict[str, dict[str, dict]],
) -> tuple[int, dict[str, int]]:
    """Estimate prompts that will fire in interactive mode.

    Returns ``(cross_sidecar_total, per_sidecar_in_file_total)``. Simulates
    the main pipeline assuming the user picks 'merge_keep_current' for each
    candidate (the most common choice); if you instead pick 'distinct' early
    on, later counts can drift slightly. Good enough for a progress display.
    """
    cross_total = 0
    in_file_total: dict[str, int] = {s.name: 0 for s in sidecars}

    groups: list[Group] = []
    group_by_tag: dict[str, Group] = {}
    same_sidecar: dict[tuple[str, str], Group] = {}
    placed: set[str] = set()

    for s in sidecars:
        for e in entries_by_sidecar[s.name]:
            rec = existing_maps[s.name].get(e.local_tag)
            if rec is None or rec.get("reason") != "assigned":
                continue
            gt = rec["global_tag"]
            if gt in group_by_tag:
                continue
            g = Group(canonical=e, global_tag=gt)
            groups.append(g)
            group_by_tag[gt] = g
            same_sidecar.setdefault((e.sidecar, e.base_local_tag), g)
            placed.add(e.ref)

    for s in sidecars:
        for e in entries_by_sidecar[s.name]:
            rec = existing_maps[s.name].get(e.local_tag)
            if rec is None or rec.get("reason") != "merged-away":
                continue
            gt = rec["global_tag"]
            g = group_by_tag.get(gt)
            if g is None:
                continue
            g.add(e)
            same_sidecar.setdefault((e.sidecar, e.base_local_tag), g)
            placed.add(e.ref)

    for s in sidecars:
        for entry in entries_by_sidecar[s.name]:
            if entry.ref in placed:
                continue
            forced = same_sidecar.get((entry.sidecar, entry.base_local_tag))
            if forced is not None:
                if not canonical_equal(entry, forced.canonical):
                    in_file_total[entry.sidecar] += 1
                forced.add(entry)
                placed.add(entry.ref)
                continue
            match = find_match(entry, groups)
            if match.kind in ("definitive", "canonical_equal"):
                match.group.add(entry)
                same_sidecar.setdefault((entry.sidecar, entry.base_local_tag), match.group)
            elif match.kind == "candidate":
                cross_total += 1
                # Pretend the user picks merge_keep_current (don't spawn a new group).
                match.group.add(entry)
                same_sidecar.setdefault((entry.sidecar, entry.base_local_tag), match.group)
            else:
                g = Group(canonical=entry)
                groups.append(g)
                same_sidecar.setdefault((entry.sidecar, entry.base_local_tag), g)
            placed.add(entry.ref)

    return cross_total, in_file_total


def load_existing_maps(
    sidecars_dir: Path, sidecars: list[Path]
) -> dict[str, dict[str, dict]]:
    out: dict[str, dict[str, dict]] = {}
    for s in sidecars:
        map_path = sidecars_dir / f"{s.stem}-citation-map.json"
        if map_path.exists():
            data = json.loads(map_path.read_text(encoding="utf-8"))
            out[s.name] = data.get("mappings", {})
        else:
            out[s.name] = {}
    return out


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--no-stop", action="store_true",
                   help="Auto-resolve duplicates (DOI/eprint/published/longest).")
    p.add_argument("--dry-run", action="store_true",
                   help="Parse and report; write nothing.")
    p.add_argument("--output", type=Path, default=DEFAULT_OUTPUT)
    p.add_argument("--sidecars-dir", type=Path, default=DEFAULT_SIDECARS_DIR)
    return p.parse_args()


def main() -> int:
    args = parse_args()

    sidecars = sorted(args.sidecars_dir.glob("arXiv-*.bib"))
    if not sidecars:
        print(
            f"No sidecars matching arXiv-*.bib in {args.sidecars_dir}",
            file=sys.stderr,
        )
        return 2

    print(
        f"Found {len(sidecars)} sidecar(s): "
        + ", ".join(s.name for s in sidecars),
        file=sys.stderr,
    )

    existing_maps = load_existing_maps(args.sidecars_dir, sidecars)

    # Cross-check existing bib if present (warn-only)
    if args.output.exists():
        with open(args.output, "r", encoding="utf-8") as f:
            existing_db = bibtexparser.load(f, parser=BibTexParser(common_strings=True))
        bib_keys = {e["ID"] for e in existing_db.entries}
        assigned_tags = {
            rec["global_tag"]
            for sidecar_map in existing_maps.values()
            for rec in sidecar_map.values()
            if rec.get("reason") == "assigned"
        }
        missing = assigned_tags - bib_keys
        if missing:
            preview = ", ".join(sorted(missing)[:5])
            print(
                f"[stale-bib] {len(missing)} mapped global tag(s) missing from "
                f"{args.output.name} (will regenerate): {preview}...",
                file=sys.stderr,
            )

    entries_by_sidecar: dict[str, list[Entry]] = {
        s.name: parse_sidecar(s) for s in sidecars
    }
    total_in = sum(len(es) for es in entries_by_sidecar.values())
    print(
        f"Parsed {total_in} entries across {len(sidecars)} sidecars.",
        file=sys.stderr,
    )

    # Reconstruct groups from existing maps (idempotency).
    # Tracks the group that the FIRST occurrence of each (sidecar, base_tag)
    # landed in, so subsequent occurrences (in-sidecar duplicates) get
    # force-merged into the same group rather than spawning a new one.
    groups: list[Group] = []
    group_by_global_tag: dict[str, Group] = {}
    same_sidecar_canonicals: dict[tuple[str, str], Group] = {}
    placed: set[str] = set()

    for s in sidecars:
        for entry in entries_by_sidecar[s.name]:
            rec = existing_maps[s.name].get(entry.local_tag)
            if rec is None or rec.get("reason") != "assigned":
                continue
            gt = rec["global_tag"]
            if gt in group_by_global_tag:
                print(
                    f"[stale-map] duplicate canonical for '{gt}': "
                    f"{group_by_global_tag[gt].canonical.ref} and {entry.ref}; "
                    "keeping first, re-processing second",
                    file=sys.stderr,
                )
                continue
            g = Group(canonical=entry, global_tag=gt)
            g.collided_tags = list(rec.get("collided_with", []))
            groups.append(g)
            group_by_global_tag[gt] = g
            same_sidecar_canonicals.setdefault((entry.sidecar, entry.base_local_tag), g)
            placed.add(entry.ref)

    for s in sidecars:
        for entry in entries_by_sidecar[s.name]:
            rec = existing_maps[s.name].get(entry.local_tag)
            if rec is None or rec.get("reason") != "merged-away":
                continue
            gt = rec["global_tag"]
            g = group_by_global_tag.get(gt)
            if g is None:
                print(
                    f"[stale-map] merged-away entry {entry.ref} -> '{gt}' has "
                    "no canonical; will re-process",
                    file=sys.stderr,
                )
                continue
            g.add(entry)
            same_sidecar_canonicals.setdefault((entry.sidecar, entry.base_local_tag), g)
            placed.add(entry.ref)

    # Process new entries. Tags are assigned incrementally so we can write
    # maps (with valid global_tags) after every interactive decision.
    claimed: set[str] = {g.global_tag for g in groups if g.global_tag}
    stats: dict[str, int] = defaultdict(int)
    interactive = not (args.no_stop or args.dry_run)

    cross_total = 0
    in_file_total: dict[str, int] = {s.name: 0 for s in sidecars}
    cross_done = 0
    in_file_done: dict[str, int] = {s.name: 0 for s in sidecars}
    if interactive:
        cross_total, in_file_total = precount_prompts(
            sidecars, entries_by_sidecar, existing_maps
        )
        per_sidecar_lines = [
            f"    {name}: {n} in-file" for name, n in in_file_total.items() if n
        ]
        print(
            f"\nInteractive prompts pending (estimate): "
            f"{cross_total} cross-sidecar + {sum(in_file_total.values())} in-file",
            file=sys.stderr,
        )
        if per_sidecar_lines:
            print("\n".join(per_sidecar_lines), file=sys.stderr)
        print("", file=sys.stderr)

    def _record_first_occurrence(entry: Entry, group: Group) -> None:
        same_sidecar_canonicals.setdefault(
            (entry.sidecar, entry.base_local_tag), group
        )

    def _new_group(entry: Entry) -> Group:
        g = Group(canonical=entry)
        groups.append(g)
        assign_one_tag(g, claimed)
        claimed.add(g.global_tag)
        _record_first_occurrence(entry, g)
        return g

    for s in sidecars:
        for entry in entries_by_sidecar[s.name]:
            if entry.ref in placed:
                continue

            forced = same_sidecar_canonicals.get(
                (entry.sidecar, entry.base_local_tag)
            )

            if forced is not None:
                # Same base local_tag already seen in this sidecar.
                # Force-merge: the .tex source cites \cite{base_tag} and the
                # rewriter can only point it at one entry, so "distinct" isn't
                # an option here.
                if canonical_equal(entry, forced.canonical):
                    stats["auto_merge_in_sidecar_canonical"] += 1
                    forced.add(entry)
                    sys.stderr.write(
                        f"[auto:in-sidecar-canon] {entry.ref} -> {forced.canonical.ref}\n"
                    )
                elif args.dry_run:
                    stats["in_sidecar_would_prompt"] += 1
                    forced.add(entry)
                else:
                    if interactive:
                        save_progress(args.sidecars_dir, sidecars, groups)
                    progress_label = None
                    if interactive:
                        in_file_done[entry.sidecar] += 1
                        progress_label = (
                            f"{entry.sidecar} in-file disambiguation "
                            f"{in_file_done[entry.sidecar]} of "
                            f"{in_file_total[entry.sidecar]}"
                        )
                    decision = resolve_candidate(
                        entry, forced, 1.0, args.no_stop,
                        force_merge=True, progress_label=progress_label,
                    )
                    if decision == "merge_keep_current":
                        stats["in_sidecar_kept_current"] += 1
                        forced.add(entry)
                    elif decision == "merge_replace":
                        stats["in_sidecar_replaced"] += 1
                        forced.add(entry)
                        forced.make_canonical(entry)
                    elif decision == "quit":
                        save_progress(args.sidecars_dir, sidecars, groups)
                        print(
                            "Aborted by user. Progress saved -- re-run to resume.",
                            file=sys.stderr,
                        )
                        return 3
                placed.add(entry.ref)
                if interactive:
                    save_progress(args.sidecars_dir, sidecars, groups)
                continue

            # First occurrence in this sidecar -- normal cross-sidecar match.
            match = find_match(entry, groups)

            if match.kind == "definitive":
                stats["auto_merge_identifier"] += 1
                match.group.add(entry)
                _record_first_occurrence(entry, match.group)
                sys.stderr.write(
                    f"[auto:id] {entry.ref} -> {match.group.canonical.ref}\n"
                )
            elif match.kind == "canonical_equal":
                stats["auto_merge_canonical"] += 1
                match.group.add(entry)
                _record_first_occurrence(entry, match.group)
                sys.stderr.write(
                    f"[auto:canon] {entry.ref} -> {match.group.canonical.ref}\n"
                )
            elif match.kind == "candidate":
                if args.dry_run:
                    stats["would_prompt"] += 1
                    _new_group(entry)
                else:
                    if interactive:
                        save_progress(args.sidecars_dir, sidecars, groups)
                    progress_label = None
                    if interactive:
                        cross_done += 1
                        progress_label = (
                            f"global cross-sidecar disambiguation "
                            f"{cross_done} of {cross_total}"
                        )
                    decision = resolve_candidate(
                        entry, match.group, match.jaccard, args.no_stop,
                        progress_label=progress_label,
                    )
                    if decision == "merge_keep_current":
                        stats["merged_kept_current"] += 1
                        match.group.add(entry)
                        _record_first_occurrence(entry, match.group)
                    elif decision == "merge_replace":
                        stats["merged_replaced"] += 1
                        match.group.add(entry)
                        match.group.make_canonical(entry)
                        _record_first_occurrence(entry, match.group)
                    elif decision == "distinct":
                        stats["kept_distinct"] += 1
                        _new_group(entry)
                    elif decision == "quit":
                        save_progress(args.sidecars_dir, sidecars, groups)
                        print(
                            "Aborted by user. Progress saved -- re-run to resume.",
                            file=sys.stderr,
                        )
                        return 3
            else:
                stats["new"] += 1
                _new_group(entry)

            placed.add(entry.ref)
            if interactive:
                save_progress(args.sidecars_dir, sidecars, groups)

    stats["tag_collisions_extended"] = sum(
        1 for g in groups if g.global_tag and g.global_tag != build_tag(g, 1)
    )

    print(f"\nGroups: {len(groups)}  (entries in: {total_in})", file=sys.stderr)
    for k, v in sorted(stats.items()):
        print(f"  {k}: {v}", file=sys.stderr)

    if args.dry_run:
        print("\n(dry-run - no files written)", file=sys.stderr)
        return 0

    write_bib(args.output, groups)
    write_maps(args.sidecars_dir, sidecars, groups)

    def _display_path(p: Path) -> str:
        try:
            return str(p.relative_to(REPO_ROOT))
        except ValueError:
            return str(p)

    print(f"\nWrote {_display_path(args.output)}", file=sys.stderr)
    print(
        f"Wrote {len(sidecars)} citation map(s) under "
        f"{_display_path(args.sidecars_dir)}/",
        file=sys.stderr,
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
